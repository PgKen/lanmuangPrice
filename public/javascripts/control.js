function locationLink(i) {
    switch (i) {
        case 1:
            location.href = "../"
            break;

        case 2:
            location.href = "/type"
            break;
        case 3:
            location.href = "/kind"
            break;
        case 4:
            location.href = "/product"
            break;
        case 5:
            location.href = "/locate"
            break;
        case 6:
            location.href = "/unitProduct"
            break;
        case 7:
            location.href = "/price/0/0"
            break;
        case 9:
            location.href = "/show/0/0"
            break;

        default:
            break;
    }
}